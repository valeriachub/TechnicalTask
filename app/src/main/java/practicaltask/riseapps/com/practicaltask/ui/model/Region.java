package practicaltask.riseapps.com.practicaltask.ui.model;

public class Region {

    private int id;
    private String title;

    public Region(int id, String title) {
        this.id = id;
        this.title = title;
    }

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }
}
